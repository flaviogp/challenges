function pickingNumbers(a) {
       let count = 0 ;
       let max = 0;
        for(let i = 0 ; i < a.length ; i++){
            count = 0;
            for(let j = 0 ; j < a.length ; j++){
                if(a[j] == a[i] || a[j] == a[i]-1){
                    count++;
                }
            }
            if(max < count) max = count;
        }
        return max;
}


